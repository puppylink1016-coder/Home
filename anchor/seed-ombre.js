async function run() {
  console.log('Waking up Render...');
  try {
    await fetch(`${API}/health`, { signal: AbortSignal.timeout(60000) });
    console.log('Server awake.');
  } catch (e) {
    console.log('Health check timeout, trying anyway...');
  }

  let success = 0;
  let fail = 0;

  for (let i = 0; i < memories.length; i++) {
    const content = memories[i];
    console.log(`[${i + 1}/${memories.length}] Storing: ${content.substring(0, 40)}...`);

    try {
      const res = await fetch(`${API}/api/ombre/memories`, {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ content }),
        signal: AbortSignal.timeout(30000),
      });

      const data = await res.json();
      if (data.success) {
        success++;
        console.log(`  OK`);
      } else {
        fail++;
        console.log(`  Failed:`, data.error || data);
      }
    } catch (e) {
      fail++;
      console.log(`  Error: ${e.message}`);
    }

    // Small delay to not overwhelm the server
    await new Promise(r => setTimeout(r, 1000));
  }

  console.log(`\nDone. ${success} saved, ${fail} failed.`);
}

run();
