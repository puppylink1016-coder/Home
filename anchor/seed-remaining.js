    const content = memories[i];
    console.log(`[${i+1}/${memories.length}] ${content.substring(0, 40)}...`);

    // Store in Ombre Brain
    try {
      await fetch(`${API}/api/ombre/memories`, {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ content }),
        signal: AbortSignal.timeout(30000),
      });
    } catch (e) { console.log('  Ombre failed:', e.message); }

